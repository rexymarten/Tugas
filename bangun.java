
public class bangun {

    private double PersegiPanjang;
    private double Trapesium;

    public void LuasBangun() {
        PersegiPanjang = 0;
        Trapesium = 0;
    }

    public double getLuas(double pan, double le) {
        return PersegiPanjang = pan * le;
    }

    public double getLuas(double a, double b, double t) {
        return Trapesium = ((a + b) * t) * 0.5;
    }
}
